    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        String student = jComboBox5.getSelectedItem().toString();
        String course = jComboBox4.getSelectedItem().toString();
        String finalString = "";
        int index = student.indexOf(" ");
        String studentID = student.substring(index+1);
        studentID = studentID.trim();
        String status = ScheduleQueries.deleteClassFromStudent(currentSemesterLabel.getText(),course, studentID);

        if (status.contains("S")){
            finalString = finalString + student + " has been dropped from " + course + "\n";
        } else{
            finalString = finalString + student + " has been dropped from the waitlist for " + course + "\n";
        }
        ArrayList<ScheduleEntry> schedules = new ArrayList<ScheduleEntry>();
        schedules = ScheduleQueries.getWaitlistedStudentsByCourse(currentSemesterLabel.getText(), course);
        if (schedules.size() != 0){
            ScheduleEntry selectedSchedule = schedules.get(0);
            String selectedScheduleStudentID = selectedSchedule.getStudentID();
            StudentEntry entry = StudentQueries.getStudent(selectedScheduleStudentID);
            ScheduleQueries.updateScheduleEntry(currentSemesterLabel.getText(), selectedSchedule);
            finalString = finalString + entry.getLastName() + "," + entry.getFirstName() + "has been scheduled into " + course + "\n";
        }
        jTextArea2.setText(finalString);
        jComboBox4.removeItemAt(jComboBox4.getSelectedIndex());
    }//GEN-LAST:event_jButton5ActionPerformed

